      TFT_RD = 1;
      TFT_WR = 1;

      TFT_CS = 0;

      Write_Command_SSD1963(0x01);   //Software Reset
      Delay_ms(10);                  //The Host Processor Must Wait 5ms Before Sending Any New Commands

      Write_Command_SSD1963(0xE2);   // SET PLL
      Write_Data_SSD1963(32);        // MULTIPLIER 10 MHz BASE CLOCK x
      Write_Data_SSD1963(2);         // DIVIDES BY THIS NUMBER + 1 DIVIDE BY 3 = 100 MHz
      Write_Data_SSD1963(0x54);      // DUMMY

      Write_Command_SSD1963(0xE0);
      Write_Data_SSD1963(0x01);      // START PLL   BIT ZERO
      Delay_ms(70);                  // Wait 100us for the PLL to Lock 08/07/21
      Write_Command_SSD1963(0xE0);
      Write_Data_SSD1963(0x03);      // BIT 1 = USE PLL AS OUTPUT BIT ZERO = ENABLE PLL 08/07/21

      Write_Command_SSD1963(0x26);   // Set Gamma Curve
      Write_Data_SSD1963(1);         // Gamma curve selection =0
      //Write_Data_SSD1963(2);       // Gamma curve selection =1
      //Write_Data_SSD1963(4);       // Gamma curve selection =2
      //Write_Data_SSD1963(8);       // Gamma curve selection =3

      Write_Command_SSD1963(0x0B);   //SET SCAN MODE    TFT_Set_Index_Ptr(0x0B);
      Write_Data_SSD1963(0);         //Top to bottom, left to right normal etc

      Write_Command_SSD1963(0x0A);
      Write_Data_SSD1963(0x1C);      //Power Mode

      Write_Command_SSD1963(0xF0);    //Set Pixel Data Interface D/C = 0
      Write_Data_SSD1963(3);          //16-bit (565 format)   011 16-bit (565 format) D/C = 1

      Write_Command_SSD1963(0x3A);       //Set Pixel Format
      Write_Data_SSD1963(0b01010000);   // bits 6,5,4  101 16-bit/pixel   111 24-bit/pixel

      Write_Command_SSD1963(0x38);     //Exit Idle Mode

      Write_Command_SSD1963(0x13);     //Enter Normal Mode;

     //Write_Command_SSD1963(0x34);    //0x34 Set_tear_off     PAGE 72 FOR TEAR
      Write_Command_SSD1963(0x35);     //0x 35 set_tear_on
      Write_Data_SSD1963(0b00000000); //PAGE 65  BIT ZERO = 1 FOR BOTH VERTICAL AND HORIZONTAL SYNC, 0 = VERTICAL SYNC ONLY

      Write_Command_SSD1963(0xB8);          //0xB8 set_gpio_conf
      Write_Command_SSD1963(0xBA);          //0xBA set_gpio_value Set GPIO value for GPIO configured as output

      Write_Command_SSD1963(0xBC);   //Set Post Proc
      Write_Data_SSD1963(65);        //40 Set the contrast value     4D == 77  127 decimal is max    4D FOR CHINA DISPLAY
